<template>
    <h5 class="mb-0">Pagos y entregas</h5>
    <label for="">Porfavor inserte el n√∫mero de cuotas a generarse</label>
    <input @change="calcFees" @keyup="calcFees" type="number" min="1" max="24" v-model="numFees" class="form-control w-25">
    <div class="row mt-3">
        <div class="col-3" v-for="fee in fees">
        <div class="card shadow-none bg-transparent border border-success p-3 mt-2">
            <p class="text-danger cursor-pointer" @click="removeFee(fee.id)"><i class="bx bx-trash"></i></p>
            <p>Fecha: {{ fee.date }}</p>
            <p>Monto: S./ {{ fee.amount }}</p>
            <p>Porcentaje: {{ fee.percentage }} %</p>
            <p>Avance: {{ fee.advance }}</p>
        </div>
        </div>
    </div>
</template>
<script>
import moment from 'moment'
export default {
    props:{
        totalFinal: Number
    },
    data(){
        return{
            numFees: 0,
            fees:[]
        }
    },
    methods:{
        calcFees(){
            if(this.numFees == 1){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal*.9),
                    percentage: 100,
                    advance: '-'
                }
                this.fees.push(fee)
            }else if(this.numFees == 2){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal*.95/this.numFees),
                    percentage: 50,
                    advance: '-'
                }
                for (let index = 0; index < this.numFees; index++) {
                    this.fees.push(fee)
                }
            }else if(this.numFees == 3){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal/this.numFees),
                    percentage: 30,
                    advance: '-'
                }
                for (let index = 0; index < this.numFees; index++) {
                    
                    if(index == 1){
                        fee.percentage = 40
                        fee.amount = parseInt(this.totalFinal*.4)
                    }else{
                        fee.percentage = 30
                        fee.amount = parseInt(this.totalFinal*.3)
                    }
                    this.fees.push({...fee})
                }
            }
            else if(this.numFees == 4){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal*1.05/this.numFees),
                    percentage: 25,
                    advance: '-'
                }
                for (let index = 0; index < this.numFees; index++) {
                    this.fees.push(fee)
                }
            }
            else if(this.numFees == 5){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal*1.1/this.numFees),
                    percentage: 20,
                    advance: '-'
                }
                for (let index = 0; index < this.numFees; index++) {
                    this.fees.push(fee)
                }
            }else if(this.numFees >= 6 && this.numFees < 12){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal*1.2/this.numFees),
                    percentage: parseFloat(100/this.numFees).toFixed(1),
                    advance: '-'
                }
                for (let index = 0; index < this.numFees; index++) {
                    this.fees.push(fee)
                }
            }else if(this.numFees >= 12 && this.numFees < 18){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal*1.3/this.numFees),
                    percentage: parseFloat(100/this.numFees).toFixed(1),
                    advance: '-'
                }
                for (let index = 0; index < this.numFees; index++) {
                    this.fees.push(fee)
                }
            }else if(this.numFees >= 18 && this.numFees < 24){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal*1.4/this.numFees),
                    percentage: parseFloat(100/this.numFees).toFixed(1),
                    advance: '-'
                }
                for (let index = 0; index < this.numFees; index++) {
                    this.fees.push(fee)
                }
            }
            else if(this.numFees == 24){
                this.fees = []
                var fee = {
                    date: moment().format('DD/MM/YYYY'),
                    amount: parseInt(this.totalFinal*1.5/this.numFees),
                    percentage: parseFloat(100/this.numFees).toFixed(1),
                    advance: '-'
                }
                console.log(fee)
                for (let index = 0; index < this.numFees; index++) {
                    fee.date = moment().add(index, 'months').format('DD/MM/YYYY')
                    this.fees.push({...fee})
                }
            }
        }
    }
}
</script>
